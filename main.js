/* ============ 料金シミュレーター ============ */
const UNIT=400000;      // 1名あたり通常価格
const RATE=0.25;        // 実質負担率（最大75%OFF）
const yen=n=>'¥'+Math.round(n).toLocaleString('ja-JP');
const ppl=document.getElementById('ppl');
function sim(){
  const n=+ppl.value;
  const normal=n*UNIT, after=normal*RATE, cut=normal-after;
  document.getElementById('pplNum').textContent=n;
  document.getElementById('normalYen').textContent=yen(normal);
  document.getElementById('afterYen').textContent=yen(after);
  document.getElementById('perHead').innerHTML=yen(after/n)+'<small>/人</small>';
  document.getElementById('cutYen').textContent=yen(cut);
  document.getElementById('barAfter').style.width=(RATE*100)+'%';
}
ppl.addEventListener('input',sim); sim();

